package com.dsa.foundation.trees.bst;

public class TreeNode{
    public int val;
    public TreeNode left;
    public TreeNode right;

    public TreeNode(){
        val = 0;
        left = null;
        right = null;
    }

    public TreeNode(int val){
        this.val = val;
        left = null;
        right = null;
    }
}
